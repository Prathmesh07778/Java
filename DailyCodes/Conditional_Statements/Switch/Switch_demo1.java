package DailyCodes.Conditional_Statements.Switch;

class Switch_demo1 {

    public static void main(String args[]) {

        int x = 3;

        switch(x) {

            case 1:
                System.out.println("One");
                break;

            case 2:
                System.out.println("Two");
                break;

            default:
                System.out.println("Error");
        }
    }
}